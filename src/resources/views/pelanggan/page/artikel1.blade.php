    <p>Deadlift adalah gerakan yang dominan pada bagian pinggul dan berfungsi untuk melatih otot tubuh bagian bawah.

        Bukan lagi jenis olahraga yang awam, deadlift dikenal dengan manfaatnya yang baik untuk otot dan kebugaran serta sering kali dimasukkan dalam rutinitas latihan/olahraga.
        
        Satu latihan deadlift dapat melatih banyak otot berbeda sehingga banyak yang menggemari olahraga ini. Sebagian orang juga menggunakan deadlift untuk melatih kekuatan tubuh dan kelompok otot tertentu.
        
        Apa itu deadlift dan bagaimana cara melakukannya? Simak penjelasan selengkapnya di bawah ini, ya!</p>
    <h1>Definisi Deadlift</h1>
    <p>Mengutip Healthline, deadlift adalah gerakan di mana kamu menurunkan pinggul dan berayun untuk mengambil beban/barbel dari lantai.

        Deadlift juga biasa disebut dengan angkat beban. Gerakan angkat beban itulah yang dimaksud dengan gerakan deadlift. Saat melakukan gerakan ini maka punggung kamu harus lurus/tegak.
        
        Itulah mengapa deadlift juga memiliki manfaat yang besar untuk lower body strength atau kekuatan bagian bawah tubuh.
        
        Biasanya olahraga ini dilakukan dengan barbel, piringan beban/pelat, atau dengan dumbel. Deadlift dikenal dengan latihan yang baik untuk membentuk atau meningkatkan massa otot dan meningkatkan kebugaran.
        
        Deadlift terkadang juga dikaitkan dengan squat padahal keduanya adalah jenis gerakan dan olahraga yang berbeda.
        
        Squat merupakan gerakan menurunkan paha ke bawah hingga sejajar dengan posisi dada tetap tegak. Squat termasuk latihan fungsional dan berbeda dengan deadlift karena tidak menggunakan beban.</p>
        <h1>Cara Melakukan deadlift</h1>
        <p>Dikutip Medical News Today, berikut adalah tiga fase dalam melakukan gerakan deadlift:</p>
        <ol>
            <h4><li>The Setup</li></h4>
            <p>The setup adalah fase persiapan pose awal sebelum melakukan gerakan. Kamu harus berdiri dengan kaki yang dibuka selebar pinggul. Gerakan awal ini penting agar manfaatnya sampai pada otot inti.

                Jika kamu menggunakan barbel, maka posisi barbel di lantai harus menyentuh tulang kering kamu atau tepat di depannya. Setelah itu baru kamu bisa melakukan gerakan awal deadlift
                
                Bersandar pada pinggul, luruskan punggung/tulang belakang dan bagian dada diangkat. Setelahnya kamu bisa memegang barbel dengan pegangan over-under, satu tangan menghadap atas dan tangan lainnya menghadap ke bawah.
                
                Setelah posisi ini maka kamu bisa melanjutkannya dengan fase the pull untuk proses mengangkat beban.</p>
            <h4><li>The Pull</li></h4>
            <p>Dalam fase the pull, kamu harus memastikan pijakan kaki kuat, setelah itu luruskan kaki dan angkat dada serta beban/barbel yang kamu gunakan. Setelahnya dorong pinggul ke bagian depan dan tarik lutut ke belakang.

                Pada fase the pull ini, perlu diingat bahwa kamu juga harus mengatur pernapasan. Jangan mencoba untuk memindahkan posisi tangan dan pegangan pada beban hingga mencapai keseimbangan.</p>
            <h4><li>The Lockout</li></h4>
            <p>Fase terakhir dalam gerakan deadlift adalah the lockout. Saat kamu sudah berhasil mengangkat beban, luruskan tulang belakang/punggung dengan tegap.

                Jeda sebentar setelah mengangkat beban, lalu turunkan beban/barbel yang kamu gunakan dengan mendorong pinggul ke belakang juga gunakan otot paha untuk membuat gerakan yang perlahan.
                
                Perlu diingat saat melakukan deadlift, tidak semua postur akan terlihat sama pada setiap individu, hal ini dikarenakan setiap individu memiliki tipe tubuh yang berbeda-beda.
                
                Selain itu, banyak yang melakukan kesalahan awal dalam deadlift yaitu dengan menggunakan beban yang terlalu berat dan pengulangan yang tidak disesuaikan dengan kemampuan tubuh.
                
                Kamu harus melakukan deadlift sesuai dengan tingkat kebugaran juga tujuan melakukan latihan dan jangan berlebihan baik dalam berat maupun pengulangan.
                
                Deadlift lebih baik difokuskan pada postur dan gerakan yang benar terlebih dahulu sebelum melakukan peningkatan latihan seperti menambah beban atau menambah resistensi.</p>
        </ol>
        <h1>Manfaat Deadlift</h1>
        <p>Deadlift dinilai sebagai olahraga yang mudah diakses oleh banyak orang dengan manfaat yang baik untuk tubuh. Menurut Verywell Fit ada beberapa manfaat dari deadlift yang bisa kamu dapatkan, di antaranya adalah:</p>
        <ol>
            <h4><li>Membangun Massa dan Kekuatan Otot</li></h4>
            <p>Seperti yang sudah dijelaskan sebelumnya bahwa deadlift sangat bermanfaat untuk membentuk dan membangun massa otot. Biasanya banyak orang yang melakukan deadlift selain membentuk otot, juga ingin mencegah muscle loss seiring bertambahnya umur.

                Kamu juga bisa menggunakan deadlift sebagai pilihan olahraga untuk meningkatkan kekuatan tubuh secara keseluruhan, kekuatan tubuh inti atau core strength, dan stabilitas tubuh.</p>
            <h4><li>Meningkatkan Metabolisme Tubuh</li></h4>
            <p>Kamu juga bisa mendapatkan manfaat lain dengan melakukan deadlift, salah satunya adalah meningkatkan metabolisme tubuh. Deadlift meningkatkan lean muscle atau otot tanpa lemak di tubuh yang membantu meningkatkan metabolisme secara keseluruhan.</p>
            <h4><li>Membangun Kesehatan Fungsional</li></h4>
            <p>Deadlift sebenarnya adalah gerakan olahraga yang 'meniru' gerakan-gerakan sehari-hari sehingga hal ini baik untuk kesehatan fungsional dan merupakan functional exercise. Dengan latihan deadlifts, kamu akan lebih mudah melakukan gerakan-gerakan mengangkat beban dalam keseharian kamu.</p>
            <h4><li>Baik untuk Postur Tubuh</li></h4>
            <p>Gerakan deadlift melatih tulang belakang, punggung, serta bahu kamu untuk kerja sama melakukan suatu gerakan. Dengan latihan deadlift secara rutin, kamu bisa meningkatkan postur tubuh.

                Selain itu, deadlift juga baik untuk pemadatan tulang yang dapat mengurangi risiko cedera dan membuat tulang lebih resisten.</p>
        </ol>
    <h1>Kesimpulan</h1>
    <p>Deadlift adalah gerakan dalam latihan yang baik untuk lower body strength atau kekuatan otot bagian bawah tubuh. Kamu bisa melakukannya di tempat gym atau di mana saja dengan menggunakan beban seperti barbel atau dumbel.

        Nah, agar tidak salah postur dan gerakan dalam latihan deadlift, kamu bisa meminta saran juga latihan dari Personal Trainer dengan bergabung disini! Olahraga jadi lebih aman dan fasilitasnya terjamin!</p>
