    <h1>Konsumsi Lebih Banyak Buah, Ini 4 Hal yang Perlu Kamu Ketahui tentang Diet Fruitarian</h1>
    <p class="author">Purbo Dwi J, Aisyah A.</p>
  </header>
  <article>
    <img src="/assets/img/banner/Buah-buahan.jpg" alt="Gambar Artikel">
    <p>Tren diet baru selalu bermunculan selama bertahun-tahun yang membuat diet semakin bervariasi. Di antara banyaknya tren diet, ada satu pola makan yang disebut fruitarian. Hampir mirip seperti vegetarian, pengikut diet fruitarian hanya mengonsumsi buah-buahan, kacang-kacangan, dan biji-bijian.

        Beberapa orang yang mengikuti diet ini mengklaim bahwa menjadi seorang fruitarian bisa meningkatkan kejernihan mental, konsentrasi dan energi, serta menyebabkan penurunan berat badan secara dramatis.
        
        Namun, belum ada bukti ilmiah yang mendukung klaim tersebut dan penurunan berat badan akibat diet ini mungkin sebenarnya tidak sehat. Dilansir dari Live Strong, <strong> 4 hal yang perlu kamu ketahui tentang diet fruitarian.</strong></p>
    <h1>Makanan yang Dikonsumsi Fruitarian</h1>
    <p>
        Sebagian besar orang yang menjalani diet ini mencoba mendapatkan sekitar 75 persen volume makanan mereka dari buah-buahan. Sisanya berasal dari kacang-kacangan dan biji-bijian. Orang yang mengikuti diet ini mengklaim bahwa diet fruitarian terdiri dari tujuh kelompok makanan dasar, yaitu:
        <ul type="disc">
            <li>Buah-buahan asam seperti jeruk, cranberry, delima, dan stroberi.</li>
            <li>Buah-buahan sub-asam seperti ceri, apel, raspberry, mangga, blueberry, persik, dan pir‌.</li>
            <li>Buah-buahan manis ‌seperti pisang, melon, dan anggur‌.</li>
            <li>Buah-buahan kering ‌seperti kurma.</li>
            <li>Buah-buahan mengandung lemak seperti ‌kelapa, alpukat, dan zaitun‌.</li>
            <li>Berbagai jenis kacang-kacangan dan biji-bijian.</li>
        </ul><br>
        Karena buah rendah kalori, sulit untuk memenuhi kebutuhan kalori harian kamu. Agar tetap sehat, rata-rata orang dewasa harus mengonsumsi 1.200 hingga 1.500 kalori per hari. Ini berarti kamu perlu mengonsumsi buah dalam jumlah besar setiap hari agar tetap berada dalam kisaran kalori yang sehat.
    </p>
    <h1>Makanan yang Tidak Boleh Dikonsumsi</h1>
    <p>Seorang fruitarian tidak boleh mengonsumsi protein hewani apa pun seperti telur, unggas, dan daging sapi. Selain itu, produk susu juga tidak diperbolehkan untuk dikonsumsi fruitarian. Untuk mengatasinya, seorang fruitarian menggantinya dengan meminum sari almond, sari jambu mete, atau santan sebagai pengganti susu.</p>
    <h1>Kerugian dan Risiko Diet Fruitarian</h1>
    <p>Hanya mengonsumi buah-buahan per hari bisa menyebabkan dampak kesehatan yang negatif seperti malnutrisi dan kekurangan vitamin, termasuk vitamin B12, kalsium, seng, dan protein, terutama jika kamu tidak merencanakan pola makan dengan cermat.

        Kekurangan vitamin dan mineral ini bisa menyebabkan anemia, kelelahan, dan melemahnya sistem kekebalan tubuh. Menurut Cleveland Clinic, masalah lain yang berpotensi terjadi akibat menjalankan diet fruitarian antara lain diabetes, kerusakan gigi, dan gangguan makan.</p>
    <h1>Diet Fruitarian dan Penurunan Berat Badan</h1>
    <p>Untuk menurunkan berat badan, umumnya kamu harus mengonsumsi sedikit kalori daripada yang kamu bakar setiap hari. Meskipun diet fruitarian hanya mengonsumsi buah-buahan yang notabenenya rendah kalori, bukan berarti diet ini menjadi pilihan penurunan berat badan yang sehat atau berkelanjutan.

        Salah satu cara untuk memastikan kamu tidak menurunkan berat badan terlalu cepat dengan diet fruitarian adalah dengan mengonsumsi lebih banyak buah-buahan padat kalori seperti alpukat atau kurma. Misalnya, satu buah alpukat berukuran sedang memiliki sekitar 240 kalori dan kurma memiliki sekitar 415 kalori per cup.
        
        Perlu diperhatikan, sebelum kamu mencoba diet apa pun, ada baiknya untuk berkonsultasi terlebih dahulu dengan ahlinya, ya!</p>
